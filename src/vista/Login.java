public void Llenar()throws Exception{
        try{
            Conexion.Conectar();
            String sql="SELECT usuario FROM tipousuario";
            ResultSet rs=Conexion.Consultar(sql);
             while(rs.next()){
               String tmpStrObtenido = rs.getString("usuario");
               txttipo.addItem(tmpStrObtenido);
           }
        } catch (Exception e) {
            throw new Exception("No se Puede Llenar"+e.getMessage());
        }
    }
public int Obtener( String nombre) throws Exception{
    Conexion.Conectar();
    String sql="Select tipoUsuario from tipousuario where usuario='"+nombre+"'";
    ResultSet rs=Conexion.Consultar(sql);
     while(rs.next()){
       codigo=Integer.parseInt(rs.getString("tipoUsuario"));
    }
    Conexion.Desconectar();
        return codigo;
}
